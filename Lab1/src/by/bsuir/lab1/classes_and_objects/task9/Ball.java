package by.bsuir.lab1.classes_and_objects.task9;

public class Ball {
    private double weight;
    private String color;

    public Ball(double weight, String color) {
        this.weight = weight;
        this.color = color;
    }

    public double getWeight() {
        return this.weight;
    }

    public String getColor() {
        return this.color;
    }
}
